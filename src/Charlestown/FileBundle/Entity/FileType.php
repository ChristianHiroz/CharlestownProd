<?php

namespace Charlestown\FileBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * FileType
 *
 * @ORM\Table()
 * @ORM\Entity(repositoryClass="Charlestown\FileBundle\Entity\FileTypeRepository")
 */
class FileType
{
    /**
     * @var integer
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="type", type="string", length=255)
     */
    private $type;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set type
     *
     * @param string $type
     *
     * @return FileType
     */
    public function setType($type)
    {
        $this->type = $type;

        return $this;
    }

    /**
     * Get type
     *
     * @return string
     */
    public function getType()
    {
        return $this->type;
    }

    public function __toString(){
        return $this->type;
    }
}

